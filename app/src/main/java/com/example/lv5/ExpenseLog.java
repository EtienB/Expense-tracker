package com.example.lv5;

public class ExpenseLog
{
    private String name;
    private float value;
    private String date;
    private String category;

    public ExpenseLog(String name, float value, String date, String category)
    {
        this.name = name;
        this.value = value;
        this.date = date;
        this.category = category;
    }
}
